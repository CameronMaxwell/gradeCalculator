    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        // Gets first name, last name, and mark variables from user
        FirstName = FirstNameInput.getText();
        LastName = LastNameInput.getText();
        float mark1 = Float.parseFloat(TestOneInput.getText());
        float mark2 = Float.parseFloat(TestTwoInput.getText());
        float mark3 = Float.parseFloat(TestThreeInput.getText());
        float mark4 = Float.parseFloat(TestFourInput.getText());
        
        // Creates the object 
        StudentGradesJava st1 = new StudentGradesJava(FirstName,LastName,mark1,mark2,mark3,mark4);
        
        // Adds the object to a list everytime you add another student     
        students.add(st1);
        
        // Outputs the student info that user added
        OutputMessage.setText(st1.toString());
    }//GEN-LAST:event_AddButtonActionPerformed

    private void ListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListButtonActionPerformed
        // TODO add your handling code here:
        
        // Outputs sentence above the list
        OutputMessage.setText("First Name\tLast Name\tMark1\tMark2\tMark3\tMark4\n");
        
        // For loop that prints it all in the list 
        for (int counter = 0; counter < students.size(); counter++)  {
            OutputMessage.append(students.get(counter).toString() + "\n");
        }
    }//GEN-LAST:event_ListButtonActionPerformed

    private void CourseAverageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CourseAverageActionPerformed
        // TODO add your handling code here:
        
        // Creates the course average variable 
        float CourseAverage = 0;
        
        // Gets size of student list and calculates the average of each student in the list 
        for (int i =0; i<students.size(); i++){
            StudentGradesJava st1 = (StudentGradesJava) students.get(i);
            // Adds all of the course averages together 
            CourseAverage+=st1.getAverage();
        }
        
        // Divides the CourseAverage by the number of students in the list
        CourseAverage = CourseAverage/students.size();
        
        // Prints course average
        OutputMessage.setText("Course Average: "+CourseAverage+"% \n");
    }//GEN-LAST:event_CourseAverageActionPerformed

