    private void StudentAverageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentAverageActionPerformed
        // TODO add your handling code here:
        
        // Gets the first and last name variables
        FirstName = FirstNameInput.getText();
        LastName = LastNameInput.getText();
        
        // Gets the average for the currently entered student, program will not run if the student is not in the list
        // Then prints the students 
        for (int i = 0; i<students.size(); i++) {
            StudentGradesJava st1 =(StudentGradesJava) students.get(i);
        if (st1.getFirstName().equals(FirstName)){
            if (st1.getLastName().equals(LastName)){
                float StudentAverage = st1.getAverage();
                OutputMessage.setText(FirstName+"  "+LastName+": "+StudentAverage+"% \n");
            }
        }
        }
       
      
    }//GEN-LAST:event_StudentAverageActionPerformed

